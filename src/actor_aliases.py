_CANON = {v: canon for canon, variants in _GROUPS.items() for v in variants}


def canonicalize_actor(name: str) -> str:
    if not name:
        return name
    return _CANON.get(str(name).strip().lower(), name)


def canonicalize_actors(names):
    """Canonicalize a list and drop duplicates a merge creates."""
    out = []
    for n in names or []:
        c = canonicalize_actor(n)
        if c not in out:
            out.append(c)
    return out